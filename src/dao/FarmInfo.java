package dao;

public class FarmInfo {
	private int farmNum;
	private String id;
	private String farmHello;
	private String farmName;
	private int farmCount;
	private int farmPop;
	private String farmBGCol;
	public int getFarmNum() {
		return farmNum;
	}
	public void setFarmNum(int farmNum) {
		this.farmNum = farmNum;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFarmHello() {
		return farmHello;
	}
	public void setFarmHello(String farmHello) {
		this.farmHello = farmHello;
	}
	public String getFarmName() {
		return farmName;
	}
	public void setFarmName(String farmName) {
		this.farmName = farmName;
	}
	public int getFarmCount() {
		return farmCount;
	}
	public void setFarmCount(int farmCount) {
		this.farmCount = farmCount;
	}
	public int getFarmPop() {
		return farmPop;
	}
	public void setFarmPop(int farmPop) {
		this.farmPop = farmPop;
	}
	public String getFarmBGCol() {
		return farmBGCol;
	}
	public void setFarmBGCol(String farmBGCol) {
		this.farmBGCol = farmBGCol;
	}
	
}