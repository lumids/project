package dao;

public class MapItemGetInfo {
	private int mapItemNum;
	private String id;
	private String mapItemGetChk;
	public int getMapItemNum() {
		return mapItemNum;
	}
	public void setMapItemNum(int mapItemNum) {
		this.mapItemNum = mapItemNum;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMapItemGetChk() {
		return mapItemGetChk;
	}
	public void setMapItemGetChk(String mapItemGetChk) {
		this.mapItemGetChk = mapItemGetChk;
	}
	
	
}