package dao;

public class MapInfo {
	private int mapNum;
	private String id;
	private float x;
	private float y;
	public int getMapNum() {
		return mapNum;
	}
	public void setMapNum(int mapNum) {
		this.mapNum = mapNum;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	
}