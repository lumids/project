package dao;

public class ItemDetail {
	private int itemDetailNum;
	private int itemNum;
	private String id;
	private String itemUseChk;
	public int getItemDetailNum() {
		return itemDetailNum;
	}
	public void setItemDetailNum(int itemDetailNum) {
		this.itemDetailNum = itemDetailNum;
	}
	public int getItemNum() {
		return itemNum;
	}
	public void setItemNum(int itemNum) {
		this.itemNum = itemNum;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getItemUseChk() {
		return itemUseChk;
	}
	public void setItemUseChk(String itemUseChk) {
		this.itemUseChk = itemUseChk;
	}
	
}