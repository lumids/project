package dao;

public class MapItemInfo {
	private int mapItemNum;
	private String mapItemName;
	private String mapEventChk;
	private int mapItemPrice;
	private String mapItemPic;
	public int getMapItemNum() {
		return mapItemNum;
	}
	public void setMapItemNum(int mapItemNum) {
		this.mapItemNum = mapItemNum;
	}
	public String getMapItemName() {
		return mapItemName;
	}
	public void setMapItemName(String mapItemName) {
		this.mapItemName = mapItemName;
	}
	public String getMapEventChk() {
		return mapEventChk;
	}
	public void setMapEventChk(String mapEventChk) {
		this.mapEventChk = mapEventChk;
	}
	public int getMapItemPrice() {
		return mapItemPrice;
	}
	public void setMapItemPrice(int mapItemPrice) {
		this.mapItemPrice = mapItemPrice;
	}
	public String getMapItemPic() {
		return mapItemPic;
	}
	public void setMapItemPic(String mapItemPic) {
		this.mapItemPic = mapItemPic;
	}
}